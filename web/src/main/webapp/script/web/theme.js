window.SphinxRtdTheme = (function(jquery) {
	var stickyNav = (function() {
		var navBar, win, stickyNavCssClass = 'stickynav', applyStickNav = function() {
			if (navBar.height() <= win.height()) {
				navBar.addClass(stickyNavCssClass);
			} else {
				navBar.removeClass(stickyNavCssClass);
			}
		}, enable = function() {
			applyStickNav();
			win.resize(applyStickNav);
		}, init = function() {
			navBar = jquery('nav.wy-nav-side:first');
			win = jquery(window);
		};
		jquery(init);
		return {
			enable : enable
		};
	}());
	return {
		StickyNav : stickyNav
	};
}($));
